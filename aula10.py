height = int(input('Qual é a altura da parede?'))
print(f'A altura foi definida em {height} metros!')
width = int(input('Qual é a largura da parede?'))
print(f'A largura foi definida em {width} metros!')

area = height * width

print(f'A área da parede é de {area} metros!')

ink = area / 2

print(f'A quantidade de tinta necessária para pintala é de {ink} litros!')

